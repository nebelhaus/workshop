#!/usr/bin/env node
// Keybinding drift tripwire.
//
// The rice generates its live cheatsheet from modules/prowl/wm-bindings.nix,
// so binding and caption can never disagree *inside* an install. This site's
// keybinding pages are prose, though — nothing ties them to that table, and
// they have drifted before (⌥1–4 lived in the docs long after the rice moved
// workspace focus to the caps leader).
//
// This script snapshots the rice's binding surface (the wm-bindings table,
// plus the launch/resize mode keys from aerospace.toml) into
// web/src/data/rice-bindings.json. CI re-derives the snapshot from the rice's
// current main and fails on any difference — the failure is the signal that a
// human must update the docs (reference/keybindings.md, and usually
// guides/window-management.mdx + start/first-run.mdx) and then refresh the
// snapshot with --update.
//
// Usage:
//   node web/scripts/check-rice-bindings.mjs --rice <nebelhaus-checkout>
//   node web/scripts/check-rice-bindings.mjs --rice <nebelhaus-checkout> --update
//
// Needs `nix` on PATH (pure eval of a plain .nix file; no network, no builds).

import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const SNAPSHOT = join(here, '../src/data/rice-bindings.json');

const args = process.argv.slice(2);
const update = args.includes('--update');
const riceIdx = args.indexOf('--rice');
const rice = riceIdx >= 0 ? args[riceIdx + 1] : process.env.RICE_DIR;
if (!rice) {
  console.error('usage: check-rice-bindings.mjs --rice <nebelhaus-checkout> [--update]');
  process.exit(2);
}

// The static chord table — pure data, evaluated without fetching anything.
const wmBindings = JSON.parse(
  execFileSync('nix', ['eval', '--json', '--file', join(rice, 'modules/prowl/wm-bindings.nix')], {
    encoding: 'utf8',
  }),
);

// The mode keys that live in aerospace.toml rather than the table. Keys only:
// commands carry @HOME@/@BIN@ noise that would churn the snapshot for free.
// Line-based: a naive substring split gets fooled by comments that *mention*
// a section header.
function modeKeys(toml, mode) {
  const keys = [];
  let inSection = false;
  for (const line of toml.split('\n')) {
    if (line.startsWith('[')) {
      // Any table header ends the current section — including [[array]] ones.
      inSection = line.trim() === `[mode.${mode}.binding]`;
      continue;
    }
    if (!inSection) continue;
    const key = line.match(/^([\w-]+)\s*=/)?.[1];
    if (key) keys.push(key);
  }
  return keys;
}
const toml = readFileSync(join(rice, 'modules/prowl/aerospace.toml'), 'utf8');

const current = JSON.stringify(
  {
    _note: 'Generated by web/scripts/check-rice-bindings.mjs --update. Do not edit by hand.',
    wmBindings,
    launchModeKeys: modeKeys(toml, 'launch'),
    resizeModeKeys: modeKeys(toml, 'resize'),
    // service mode isn't listed here: its keys are @SERVICE_STATIC@ tokens in
    // the toml, generated from wm-bindings.nix — already covered above.
  },
  null,
  2,
) + '\n';

if (update) {
  writeFileSync(SNAPSHOT, current);
  console.log(`snapshot refreshed: ${SNAPSHOT}`);
  process.exit(0);
}

let committed = '';
try {
  committed = readFileSync(SNAPSHOT, 'utf8');
} catch {
  console.error(`no snapshot at ${SNAPSHOT} — run with --update once to create it.`);
  process.exit(1);
}

if (committed === current) {
  console.log('rice bindings unchanged — docs are as current as the last human pass.');
  process.exit(0);
}

console.error('✗ the rice\'s keybindings moved since the docs were last checked.\n');
const a = JSON.parse(committed);
const b = JSON.parse(current);
for (const key of ['wmBindings', 'launchModeKeys', 'resizeModeKeys']) {
  if (JSON.stringify(a[key]) !== JSON.stringify(b[key])) {
    console.error(`  changed: ${key}`);
    console.error(`    was: ${JSON.stringify(a[key])}`);
    console.error(`    now: ${JSON.stringify(b[key])}`);
  }
}
console.error(`
Update the docs to match the rice —
  web/src/content/docs/reference/keybindings.md
  web/src/content/docs/guides/window-management.mdx
  web/src/content/docs/start/first-run.mdx (the "four moves" + sequence)
— then refresh the snapshot:
  node web/scripts/check-rice-bindings.mjs --rice <nebelhaus-checkout> --update`);
process.exit(1);
